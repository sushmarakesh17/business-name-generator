import random
adjectives = ["Smart", "Bright", "Golden", "Creative", "Royal"]
nouns = ["Tech", "Solutions", "Bakery", "Foods", "Creations"]
word1 = random.choice(adjectives)
word2 = random.choice(nouns)
business_name = word1 + " " + word2
print("Suggested Business Name:", business_name)    
